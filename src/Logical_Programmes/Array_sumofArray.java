package Logical_Programmes;

import java.util.Arrays;

public class Array_sumofArray
{
    public static void main(String[] args)
    {

        int num[]={22,22,23};
        int sum=0;

        for (int i=0;i<num.length;i++)
        {
            sum=sum + num[i];
        }
        System.out.println(sum);




    }
}
