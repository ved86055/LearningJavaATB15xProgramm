package Arrays.MultiDimensionalArray;

public class Basic_2ndWay
{
    public static void main(String[] args)
    {

      int ary[][]={{12,22,32},{13,14,15}};


      for (int i=0;i<2;i++)
      {

          for (int j=0;j<3;j++)
          {

              System.out.println(ary[i][j]);

          }


      }


    }
}
