package For_Loop;

public class For_if_loop
{
    public static void main(String[] args)
    {
        for (int ved=0;ved<=18;ved++)
        {
            if (ved>15)
            {
                System.out.println("gift for ved");
            }
            else {
                System.out.println("no gift");
            }




        }
    }
}
