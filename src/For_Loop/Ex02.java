package For_Loop;

public class Ex02
{
    public static void main(String[] args)
    {

        for (int i=10;i>-10;i--)
        {
            System.out.println(i);
        }
        System.out.println("------------------------------------");
        for (int k =50;k>0;k--)
        {
            System.out.println(k);
        }
        System.out.println("-----------------------------------------");
        for (int l=1;l<=20;l++)
        {
            System.out.println(l);
        }
    }
}
